function U=get_U(X)
% 这个是根据动力学的simulink改的
U=zeros(3,1);
x=X(1);
y=X(2);
z=X(3);
rt=sqrt(x^2+y^2+z^2);
K=asin(z/rt);%纬度
L=atan(y/x);%经度
c20=-0.113;
c22=0.0396;
re =1750;%切面半径
Mu=4.794e3;%引力常数
w1=1-((1-5*(sin(L))^2)*1.5*c20+(2-5*cos(2*K)*(cos(L))^2)*3*c22)*re^2/2/rt^2;
w2=1-((1-5*(sin(L))^2)*1.5*c20-(2+5*cos(2*K)*(cos(L))^2)*3*c22)*re^2/2/rt^2;
w3=1-((1-5*(sin(L))^2)*1.5*c20+(cos(2*K)*(cos(L))^2*15*c22))*re^2/2/rt^2;
U(1)=(-Mu)*w1/rt^3;
U(2)=(-Mu)*w2/rt^3;
U(3)=(-Mu)*w3/rt^3;
